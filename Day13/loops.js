/*
  the three types of loops in js are;

  1.for loops (common):
  for(start;end;increment/decrement){
    code
  }

  2. while loops:
  start
  while(condition/end){
    increment(pre)
    code
    increment (post)
  }

  3.do..while :
  start
  do{
    increment(pre)
    code
    increment (post)
  }while(conditon/end)

  4.for...of loop :
    for(const iterator of object){
    code
    }

  5.for...each loop
  object.forEach( i => {
    code
    })  

  6. for..in loop
  for(const key in object) {
  code
  } 

  7. map:
    array.map(i =>{
    code
    })

*/